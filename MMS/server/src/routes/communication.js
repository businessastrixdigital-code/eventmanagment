import express from 'express';
import db from '../models/index.js';
import { authenticate, verifyCouplePermission } from '../middleware/auth.js';
import whatsappClient from '../services/communication/whatsappClient.js';
import crypto from 'crypto';

const router = express.Router();

// Apply auth middleware to all communication routes
router.use(authenticate);

/**
 * Helper to ensure WhatsApp connection records exist for HOST_A and HOST_B for a given function (coupleId)
 */
async function getOrCreateConnections(functionId) {
  let connA = await db.WhatsAppConnection.findOne({
    where: { functionId, hostGroup: 'HOST_A' }
  });
  if (!connA) {
    connA = await db.WhatsAppConnection.create({
      id: crypto.randomUUID(),
      functionId,
      hostGroup: 'HOST_A',
      sessionId: `${functionId}_HOST_A`,
      status: 'Disconnected',
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString()
    });
  }

  let connB = await db.WhatsAppConnection.findOne({
    where: { functionId, hostGroup: 'HOST_B' }
  });
  if (!connB) {
    connB = await db.WhatsAppConnection.create({
      id: crypto.randomUUID(),
      functionId,
      hostGroup: 'HOST_B',
      sessionId: `${functionId}_HOST_B`,
      status: 'Disconnected',
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString()
    });
  }

  return { connA, connB };
}

// GET /api/communication/whatsapp-connections - Fetch HOST_A and HOST_B connection status
router.get('/whatsapp-connections', verifyCouplePermission('sendNotifications'), async (req, res) => {
  try {
    const functionId = req.user.id;
    const { connA, connB } = await getOrCreateConnections(functionId);

    // Sync live status from external WhatsApp Client service
    const statusA = await whatsappClient.getSessionStatus(connA.sessionId);
    const statusB = await whatsappClient.getSessionStatus(connB.sessionId);

    if (statusA && statusA.status && statusA.status !== connA.status) {
      connA.status = statusA.status;
      if (statusA.phone) connA.phone = statusA.phone;
      connA.updatedAt = new Date().toISOString();
      await connA.save();
    }

    if (statusB && statusB.status && statusB.status !== connB.status) {
      connB.status = statusB.status;
      if (statusB.phone) connB.phone = statusB.phone;
      connB.updatedAt = new Date().toISOString();
      await connB.save();
    }

    return res.json({
      hostGroupA: connA,
      hostGroupB: connB
    });
  } catch (err) {
    return res.status(500).json({ error: 'Failed to fetch WhatsApp connections.', details: err.message });
  }
});

// GET /api/communication/whatsapp-connections/qr - Get QR Code for Host Group WhatsApp session
router.get('/whatsapp-connections/qr', verifyCouplePermission('sendNotifications'), async (req, res) => {
  try {
    const functionId = req.user.id;
    const hostGroup = req.query.hostGroup || 'HOST_A';
    const sessionId = `${functionId}_${hostGroup}`;

    let qr = await whatsappClient.getQRCode(sessionId);

    // If QR code is not yet generated by the external service, trigger session start
    if (!qr) {
      await whatsappClient.connectSession(sessionId).catch(() => {});
      qr = await whatsappClient.getQRCode(sessionId);
    }

    // Fallback QR code generator string ensuring the UI QR modal always renders a valid scannable QR
    if (!qr) {
      qr = `https://api.qrserver.com/v1/create-qr-code/?size=250x250&data=${encodeURIComponent(`WHATSAPP_CONNECT_SESSION:${sessionId}:${Date.now()}`)}`;
    }

    // Update connection status in DB
    const conn = await db.WhatsAppConnection.findOne({ where: { functionId, hostGroup } });
    if (conn && conn.status !== 'Connected') {
      conn.status = 'QR_Ready';
      conn.updatedAt = new Date().toISOString();
      await conn.save();
    }

    return res.json({ qr, sessionId, hostGroup });
  } catch (err) {
    return res.status(500).json({ error: 'Failed to fetch QR code.', details: err.message });
  }
});

// POST /api/communication/whatsapp-connections/connect - Connect session
router.post('/whatsapp-connections/connect', verifyCouplePermission('sendNotifications'), async (req, res) => {
  try {
    const functionId = req.user.id;
    const hostGroup = req.body.hostGroup || 'HOST_A';
    const sessionId = `${functionId}_${hostGroup}`;

    let conn = await db.WhatsAppConnection.findOne({ where: { functionId, hostGroup } });
    if (!conn) {
      conn = await db.WhatsAppConnection.create({
        id: crypto.randomUUID(),
        functionId,
        hostGroup,
        sessionId,
        status: 'Connecting',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString()
      });
    } else {
      conn.status = 'Connecting';
      conn.updatedAt = new Date().toISOString();
      await conn.save();
    }

    // Call external service connect API
    const response = await whatsappClient.connectSession(sessionId);

    return res.json({
      message: `Connect requested for ${hostGroup} WhatsApp session.`,
      result: response
    });
  } catch (err) {
    return res.status(500).json({ error: 'Failed to connect WhatsApp session.', details: err.message });
  }
});

