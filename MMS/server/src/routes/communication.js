// GET /api/communication/delivery-logs - Fetch communication logs
router.get('/delivery-logs', verifyCouplePermission('sendNotifications'), async (req, res) => {
  try {
    const functionId = req.user.id;
    const hostGroup = req.user.hostGroup;

    const whereClause = { functionId };
    if (hostGroup) {
      whereClause.hostGroup = hostGroup;
    }

    const logs = await db.CommunicationLog.findAll({
      where: whereClause,
      order: [['createdAt', 'DESC']]
    });

    return res.json({ data: logs });
  } catch (err) {
    return res.status(500).json({ error: 'Failed to fetch delivery logs.', details: err.message });
  }
});

// POST /api/communication/delivery-logs/:id/retry - Retry sending a failed message
router.post('/delivery-logs/:id/retry', verifyCouplePermission('sendNotifications'), async (req, res) => {
  try {
    const functionId = req.user.id;
    const log = await db.CommunicationLog.findOne({
      where: { id: req.params.id, functionId }
    });

    if (!log) return res.status(404).json({ error: 'Delivery log record not found.' });

    // Identify hostGroup session for auto routing
    const targetHostGroup = log.hostGroup || 'HOST_A';
    const sessionId = `${functionId}_${targetHostGroup}`;

    // Verify session connection status
    const conn = await db.WhatsAppConnection.findOne({
      where: { functionId, hostGroup: targetHostGroup }
    });

    if (!conn || conn.status !== 'Connected') {
      log.error = `Cannot retry: WhatsApp session for ${targetHostGroup} is disconnected.`;
      log.status = 'Failed';
      log.retryCount = (log.retryCount || 0) + 1;
      log.updatedAt = new Date().toISOString();
      await log.save();

      return res.status(400).json({
        error: `WhatsApp connection for ${targetHostGroup} is disconnected.`
      });
    }

    // Attempt resend via whatsappClient
    let result;
    if (log.notificationId) {
      const notif = await db.Notification.findByPk(log.notificationId);
      if (notif) {
        result = await whatsappClient.sendTextMessage(sessionId, log.phone, notif.template);
      }
    }

    if (result && result.ok) {
      log.status = 'Sent';
      log.error = null;
      log.sentAt = new Date().toISOString();
    } else {
      log.status = 'Failed';
      log.error = result ? (result.error || 'Failed retry request.') : 'Retry failed.';
    }

    log.retryCount = (log.retryCount || 0) + 1;
    log.updatedAt = new Date().toISOString();
    await log.save();

    return res.json({ success: true, log });
  } catch (err) {
    return res.status(500).json({ error: 'Failed to retry delivery log.', details: err.message });
  }
});

export default router;
